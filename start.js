function line() {
    console.log(`${c.dim}в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${c.reset}`);
}

function info(msg) {
    console.log(`${c.cyan}вһң${c.reset} ${msg}`);
}

function success(msg) {
    console.log(`${c.green}вң”${c.reset} ${msg}`);
}

function warn(msg) {
    console.log(`${c.yellow}вҡ ${c.reset} ${msg}`);
}

function error(msg) {
    console.log(`${c.red}вңҳ${c.reset} ${msg}`);
}

function runCommand(command, title) {
    try {
        info(title);
        console.log(`${c.dim}${command}${c.reset}\n`);

        execSync(command, { stdio: 'inherit' });

        success(`${title} complete\n`);
    } catch (err) {
        error(`${title} failed`);
        process.exit(1);
    }
}

// в”Җв”Җв”Җ Start в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
